fn variant_type_names(variants: &BTreeMap<String, Vec<TypeRef>>) -> BTreeMap<String, String> {
    let mut used = BTreeSet::new();
    variants
        .keys()
        .map(|variant| {
            (
                variant.clone(),
                unique_identifier(variant_type_name(variant), &mut used),
            )
        })
        .collect()
}

fn unique_identifier(base: String, used: &mut BTreeSet<String>) -> String {
    if used.insert(base.clone()) {
        return base;
    }

    for suffix in 2.. {
        let candidate = format!("{base}{suffix}");
        if used.insert(candidate.clone()) {
            return candidate;
        }
    }
    unreachable!("unbounded suffix search always returns")
}

fn allowed_value_kinds(type_ref: &TypeRef) -> Vec<&'static str> {
    let mut kinds = Vec::new();
    collect_allowed_value_kinds(type_ref, &mut kinds);
    kinds.sort_unstable();
    kinds.dedup();
    kinds
}

fn collect_allowed_value_kinds(type_ref: &TypeRef, kinds: &mut Vec<&'static str>) {
    match type_ref {
        TypeRef::Number => kinds.push(ValueKind::Number.into()),
        TypeRef::Unsigned => kinds.push(ValueKind::Unsigned.into()),
        TypeRef::Float => kinds.push(ValueKind::Float.into()),
        TypeRef::Symbol => kinds.push(ValueKind::Symbol.into()),
        TypeRef::Nullary => kinds.push(ValueKind::Nullary.into()),
        TypeRef::Record(_) => kinds.push(ValueKind::Record.into()),
        TypeRef::List(_) => kinds.push(ValueKind::List.into()),
        TypeRef::Adt { .. } => kinds.push(ValueKind::Adt.into()),
        TypeRef::Reference { runtime, .. } => kinds.push(value_kind_name(*runtime)),
        TypeRef::Subtype { base, .. } => collect_allowed_value_kinds(base, kinds),
        TypeRef::Declared { runtime, .. } => collect_allowed_value_kinds(runtime, kinds),
        TypeRef::Union { variants, .. } => {
            for variant in variants {
                collect_allowed_value_kinds(variant, kinds);
            }
        }
    }
}

