fn pascal_case(value: &str) -> String {
    let mut result = String::new();
    let mut uppercase_next = true;
    for ch in value.chars() {
        if ch.is_ascii_alphanumeric() {
            if uppercase_next {
                result.push(ch.to_ascii_uppercase());
                uppercase_next = false;
            } else {
                result.push(ch);
            }
        } else {
            uppercase_next = true;
        }
    }

    if result.is_empty() {
        "Relation".to_owned()
    } else if result.chars().next().is_some_and(|ch| ch.is_ascii_digit()) {
        format!("Relation{result}")
    } else {
        result
    }
}

fn field_name(attribute_name: &str, index: usize) -> String {
    let mut result = String::new();
    for ch in attribute_name.chars() {
        if ch == '_' || ch.is_ascii_alphanumeric() {
            result.push(ch.to_ascii_lowercase());
        } else {
            result.push('_');
        }
    }
    if result.is_empty() || result.chars().next().is_some_and(|ch| ch.is_ascii_digit()) {
        result = format!("column_{index}");
    }

    if rust_keywords().contains(result.as_str()) {
        format!("r#{result}")
    } else {
        result
    }
}

